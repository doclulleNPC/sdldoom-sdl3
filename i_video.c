//
// Build a DOOM mouse event from the current SDL mouse button mask.
//
static int I_MouseButtons(SDL_MouseButtonFlags state)
{
    return 0
	| ((state & SDL_BUTTON_MASK(SDL_BUTTON_LEFT))   ? 1 : 0)
	| ((state & SDL_BUTTON_MASK(SDL_BUTTON_MIDDLE)) ? 2 : 0)
	| ((state & SDL_BUTTON_MASK(SDL_BUTTON_RIGHT))  ? 4 : 0);
}

/* This processes SDL events */
void I_GetEvent(SDL_Event *Event)
{
    event_t event;

    switch (Event->type)
    {
      case SDL_EVENT_KEY_DOWN:
	event.type = ev_keydown;
	// MOD: toggle the console on the *physical* grave/tilde key (the key
	// left of 1), regardless of layout -- on a German keyboard that key
	// emits '^'/'°', not '`', so match by scancode and feed the console
	// its expected '`' (CON_TOGGLE).
	event.data1 = (Event->key.scancode == SDL_SCANCODE_GRAVE)
		    ? '`' : xlatekey(Event->key.key);
	D_PostEvent(&event);
        break;

      case SDL_EVENT_KEY_UP:
	event.type = ev_keyup;
	event.data1 = (Event->key.scancode == SDL_SCANCODE_GRAVE)
		    ? '`' : xlatekey(Event->key.key);
	D_PostEvent(&event);
	break;

      case SDL_EVENT_MOUSE_BUTTON_DOWN:
      case SDL_EVENT_MOUSE_BUTTON_UP:
	event.type = ev_mouse;
	event.data1 = I_MouseButtons(SDL_GetMouseState(NULL, NULL));
	event.data2 = event.data3 = 0;
	D_PostEvent(&event);
	break;

      case SDL_EVENT_MOUSE_MOTION:
	event.type = ev_mouse;
	event.data1 = I_MouseButtons(Event->motion.state);
#ifdef _WIN32
	// raw input on Windows reports device coordinates without desktop acceleration,
	// scale it up to feel comfortable compared to Linux
	event.data2 =  ((int)Event->motion.xrel) << 3;
	event.data3 = -((int)Event->motion.yrel) << 3;
#else
	event.data2 =  ((int)Event->motion.xrel) << 2;
	event.data3 = -((int)Event->motion.yrel) << 2;
#endif
	D_PostEvent(&event);
	break;

      case SDL_EVENT_MOUSE_WHEEL:
	// MOD: wheel up/down -> next/prev weapon, as synthetic key presses.
	if (Event->wheel.y != 0)
	{
	    event.type = ev_keydown;
	    event.data1 = (Event->wheel.y > 0) ? KEY_MWHEELUP : KEY_MWHEELDOWN;
	    D_PostEvent(&event);
	}
	break;

      case SDL_EVENT_WINDOW_FOCUS_GAINED:
	window_focused = 1;
	I_ApplyMouseGrab();		// re-grab (e.g. after alt-tab)
	break;

      case SDL_EVENT_WINDOW_FOCUS_LOST:
	window_focused = 0;
	I_ApplyMouseGrab();		// let go so the user can leave the window
	break;

      case SDL_EVENT_QUIT:
	I_Quit();
    }

}

