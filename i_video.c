//
// I_DrawConsole
// MOD: draw the drop-down console's text with SDL3's built-in font
// (SDL_RenderDebugText, an 8x8 glyph set bundled in SDL3 -- no extra library or
// font file). C_Drawer has already dimmed the backdrop in the 8-bit framebuffer;
// this runs after the game frame is on the renderer and before present.
// Logical presentation is SCREENWIDTH x SCREENHEIGHT, so a render scale of
// `hires` lets us place text in 320x200 (BASE) coordinates and have it scale
// with the resolution, matching the old layout.
//
void I_DrawConsole (void)
{
    int		h, y, i;
    float	s;

    if (renderer == NULL)
	return;
    h = C_BaseHeight ();		// drop height in BASE rows, 0 = closed
    if (h <= 0)
	return;

    s = (float) hires;
    SDL_SetRenderScale (renderer, s, s);
    SDL_SetRenderDrawColor (renderer, 0xd0, 0xd0, 0xd0, 0xff);

    // input line near the bottom of the panel, scrollback rising above it
    y = h - 10;
    SDL_RenderDebugText (renderer, 4, y, C_InputLine ());
    y -= 10;
    for (i = 0; i < C_ScrollCount () && y > -8; i++)
    {
	const char* line = C_ScrollLine (i);
	if (line && line[0])
	    SDL_RenderDebugText (renderer, 4, y, line);
	y -= 8;
    }

    SDL_SetRenderScale (renderer, 1.0f, 1.0f);
}

//
// I_FinishUpdate
//
void I_FinishUpdate (void)
{
    // Track menu/console state: grab while playing, release in the menu.
    I_ApplyMouseGrab();

    static int	lasttic;
    int		tics;
    int		i;
    int		x, y;
    void*	pixels;
    int		pitch;

    // draws little dots on the bottom of the screen
    if (devparm)
    {

	i = I_GetTime();
	tics = i - lasttic;
	lasttic = i;
	if (tics > 20) tics = 20;

	for (i=0 ; i<tics*2 ; i+=2)
	    screens[0][ (SCREENHEIGHT-1)*SCREENWIDTH + i] = 0xff;
	for ( ; i<20*2 ; i+=2)
	    screens[0][ (SCREENHEIGHT-1)*SCREENWIDTH + i] = 0x0;
    }

    // Expand the 8-bit palettized frame into the 32-bit streaming texture.
    if ( !SDL_LockTexture(texture, NULL, &pixels, &pitch) )
	return;

    for (y=0 ; y<SCREENHEIGHT ; y++)
    {
	Uint32*		dst = (Uint32 *)((Uint8 *)pixels + y*pitch);
	unsigned char*	src = screens[0] + y*SCREENWIDTH;

	// Fullcolor: inside the 3D view, use the truecolor framebuffer wherever
	// the 8-bit pixel still matches the pre-HUD snapshot (i.e. no overlay
	// drew there); everywhere else fall back to the palette expansion.
	if (view_truecolor
	    && y >= viewwindowy && y < viewwindowy+viewheight)
	{
	    int		vx0 = viewwindowx;
	    int		vx1 = viewwindowx + scaledviewwidth;
	    Uint32*	s32  = screen32   + y*SCREENWIDTH;
	    byte*	snap = view8snap  + y*SCREENWIDTH;

	    for (x=0 ; x<SCREENWIDTH ; x++)
	    {
		if (x >= vx0 && x < vx1 && src[x] == snap[x])
		    dst[x] = s32[x];
		else
		    dst[x] = palette[src[x]];
	    }
	}
	else
	{
	    for (x=0 ; x<SCREENWIDTH ; x++)
		dst[x] = palette[src[x]];
	}
    }
    view_truecolor = 0;		// consumed; next frame must re-capture

    SDL_UnlockTexture(texture);

    SDL_RenderClear(renderer);
    SDL_RenderTexture(renderer, texture, NULL, NULL);
    I_DrawConsole();			// MOD: console text overlay (SDL3 font)
    SDL_RenderPresent(renderer);
}


